package Day11;

public class StringTest {
    public static void main(String[] args) {
        String str = "hello";
        System.out.println(str.charAt(0)); // h
        System.out.println(str.charAt(1)); // e
        System.out.println(str.length()); // 5
        System.out.println(str.charAt(str.length()-1)); // o
    }
}
